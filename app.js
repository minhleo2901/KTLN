function renderCoefChart(ctx, coefficients, key, textColor, gridColor) {
    // Filter out intercept (const) to highlight variables
    const cleanCoefs = coefficients.filter(c => c.variable !== 'const');
    const labels = cleanCoefs.map(c => c.variable);
    const data = cleanCoefs.map(c => c.coef);
    
    const bgColors = cleanCoefs.map(c => {
        if (c.pValue < 0.05) {
            return c.coef > 0 ? 'rgba(16, 185, 129, 0.8)' : 'rgba(239, 68, 68, 0.8)';
        }
        return 'rgba(156, 163, 175, 0.2)'; // grey if non-significant
    });
    
    const borderColors = cleanCoefs.map(c => {
        if (c.pValue < 0.05) {
            return c.coef > 0 ? '#10b981' : '#ef4444';
        }
        return '#9ca3af';
    });

    charts[key] = new Chart(ctx, {
        type: 'bar',
        data: {
            labels: labels,
            datasets: [{
                label: 'Hệ số tác động',
                data: data,
                backgroundColor: bgColors,
                borderColor: borderColors,
                borderWidth: 1.5,
                borderRadius: 4
            }]
        },
        options: {
            indexAxis: 'y',
            responsive: true,
            maintainAspectRatio: false,
            plugins: {
                legend: { display: false },
                tooltip: {
                    titleFont: { family: 'Outfit', size: 12 },
                    bodyFont: { family: 'JetBrains Mono', size: 11 },
                    callbacks: {
                        afterBody: function(items) {
                            const index = items[0].dataIndex;
                            const statObj = cleanCoefs[index];
                            return [
                                `P-value: ${statObj.pValue.toFixed(4)}`,
                                `t/z-stat: ${statObj.testStat.toFixed(3)}`,
                                `Khoảng tin cậy: [${statObj.ciLower ? statObj.ciLower.toFixed(3) : '-'}, ${statObj.ciUpper ? statObj.ciUpper.toFixed(3) : '-'}]`
                            ];
                        }
                    }
                }
            },
            scales: {
                x: {
                    grid: { color: gridColor },
                    ticks: { color: textColor }
                },
                y: {
                    grid: { display: false },
                    ticks: { color: textColor, font: { family: 'JetBrains Mono', size: 10 } }
                }
            }
        }
    });
}

function updateFEVDChart(varName) {
    const matrix = parsedData.fevd[varName];
    const ctx = document.getElementById('fevd-chart').getContext('2d');
    
    if (charts.fevd) {
        charts.fevd.destroy();
    }
    
    // Extract horizons
    const labels = matrix.map(row => `Kỳ ${row.horizon}`);
    const contribVars = Object.keys(matrix[0].contributions);
    
    // Generate dataset for each contributing variable
    const datasets = contribVars.map((vName, idx) => {
        // Color mapping
        const color = varColors[vName] || varColors[vName.replace('dln_', '')] || getRandomColor(idx);
        
        return {
            label: vName.replace('dln_', ''),
            data: matrix.map(row => row.contributions[vName]),
            fill: true,
            backgroundColor: hexToRgba(color, 0.75),
            borderColor: color,
            borderWidth: 1,
            pointRadius: 0,
            pointHoverRadius: 4
        };
    });
    
    const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
    const textColor = isDark ? '#9ca3af' : '#4b5563';
    const gridColor = isDark ? 'rgba(255, 255, 255, 0.05)' : 'rgba(0, 0, 0, 0.05)';
    
    charts.fevd = new Chart(ctx, {
        type: 'line',
        data: {
            labels: labels,
            datasets: datasets
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            plugins: {
                legend: {
                    position: 'right',
                    labels: { color: textColor, font: { family: 'Outfit', size: 11 } }
                },
                tooltip: {
                    mode: 'index',
                    intersect: false,
                    callbacks: {
                        label: function(context) {
                            let label = context.dataset.label || '';
                            if (label) {
                                label += ': ';
                            }
                            if (context.parsed.y !== null) {
                                label += (context.parsed.y * 100).toFixed(2) + '%';
                            }
                            return label;
                        }
                    }
                }
            },
            scales: {
                x: {
                    grid: { display: false },
                    ticks: { color: textColor }
                },
                y: {
                    stacked: true,
                    grid: { color: gridColor },
                    max: 1.0,
                    ticks: {
                        color: textColor,
                        callback: function(value) {
                            return (value * 100) + '%';
                        }
                    }
                }
            }
        }
    });
}

// Helpers
function getRandomColor(index) {
    const list = ['#3b82f6', '#a855f7', '#f97316', '#10b981', '#eab308', '#ec4899', '#6366f1', '#14b8a6', '#f43f5e'];
    return list[index % list.length];
}

function hexToRgba(hex, alpha) {
    const r = parseInt(hex.slice(1, 3), 16);
    const g = parseInt(hex.slice(3, 5), 16);
    const b = parseInt(hex.slice(5, 7), 16);
    return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}
