function updateGrangerHeatmap() {
    const heatmap = document.getElementById('granger-heatmap');
    heatmap.innerHTML = '';
    
    const findingsList = document.getElementById('granger-findings-list');
    findingsList.innerHTML = '';
    
    // Extract unique variables involved
    const variables = [...new Set(parsedData.granger.map(g => g.source))];
    
    // Grid template configuration: 1 header col + N variables cols
    heatmap.style.gridTemplateColumns = `150px repeat(${variables.length}, 1fr)`;
    
    // 1. Render Header Row (Column labels)
    const emptyCorner = document.createElement('div');
    heatmap.appendChild(emptyCorner);
    
    variables.forEach(v => {
        const cell = document.createElement('div');
        cell.className = 'heatmap-header-cell';
        cell.textContent = v.replace('dln_', '');
        cell.title = v;
        heatmap.appendChild(cell);
    });
    
    // 2. Render Heatmap Rows
    variables.forEach(rowVar => {
        // Row header
        const rowHeader = document.createElement('div');
        rowHeader.className = 'heatmap-row-header';
        rowHeader.textContent = rowVar.replace('dln_', '');
        rowHeader.title = rowVar;
        heatmap.appendChild(rowHeader);
        
        // Cells for each column
        variables.forEach(colVar => {
            const cell = document.createElement('div');
            
            if (rowVar === colVar) {
                cell.className = 'heatmap-cell cell-self';
                cell.textContent = '-';
                cell.title = `${rowVar} tự tác động`;
            } else {
                const relation = parsedData.granger.find(g => g.source === rowVar && g.target === colVar);
                const pVal = relation ? relation.pValue : 1.0;
                
                cell.textContent = pVal.toFixed(3);
                cell.title = `${rowVar} → ${colVar} (p = ${pVal.toFixed(4)})`;
                
                if (pVal < 0.01) {
                    cell.className = 'heatmap-cell cell-sig-1';
                    
                    // Add to findings
                    const li = document.createElement('li');
                    li.innerHTML = `<code>${rowVar}</code> gây ra <code>${colVar}</code> trong ngắn hạn <strong>(Ý nghĩa &lt; 1%)</strong>.`;
                    findingsList.appendChild(li);
                } else if (pVal < 0.05) {
                    cell.className = 'heatmap-cell cell-sig-5';
                    
                    // Add to findings
                    const li = document.createElement('li');
                    li.innerHTML = `<code>${rowVar}</code> gây ra <code>${colVar}</code> trong ngắn hạn <strong>(Ý nghĩa &lt; 5%)</strong>.`;
                    findingsList.appendChild(li);
                } else if (pVal < 0.10) {
                    cell.className = 'heatmap-cell cell-sig-10';
                } else {
                    cell.className = 'heatmap-cell cell-no-sig';
                }
            }
            heatmap.appendChild(cell);
        });
    });
    
    if (findingsList.children.length === 0) {
        findingsList.innerHTML = '<li>Không phát hiện mối quan hệ nhân quả Granger ngắn hạn ở mức ý nghĩa 5%.</li>';
    }
}

function updateFEVDSelectors() {
    const selector = document.getElementById('fevd-var-selector');
    const prevSelected = selector.value;
    selector.innerHTML = '';
    
    const vars = Object.keys(parsedData.fevd);
    vars.forEach(v => {
        const opt = document.createElement('option');
        opt.value = v;
        opt.textContent = v;
        selector.appendChild(opt);
    });
    
    if (vars.includes(prevSelected)) {
        selector.value = prevSelected;
    }
    
    selector.removeEventListener('change', renderSelectedFEVD);
    selector.addEventListener('change', renderSelectedFEVD);
    
    renderSelectedFEVD();
}

function renderSelectedFEVD() {
    const varName = document.getElementById('fevd-var-selector').value;
    if (!varName || !parsedData.fevd[varName]) return;
    
    const matrix = parsedData.fevd[varName];
    
    // Draw table
    const tableHeader = document.getElementById('fevd-table-header');
    tableHeader.innerHTML = '<th>Horizon (Kỳ)</th>';
    
    const firstRowKeys = Object.keys(matrix[0].contributions);
    firstRowKeys.forEach(k => {
        const th = document.createElement('th');
        th.textContent = k;
        tableHeader.appendChild(th);
    });
    
    const tableBody = document.getElementById('fevd-table-body');
    tableBody.innerHTML = '';
    
    // Only display some key horizons (e.g. 0, 1, 2, 3, 4, 5, 10, 15, 20, 23) to keep table concise
    const keyHorizons = [0, 1, 2, 3, 4, 5, 6, 8, 10, 12, 15, 20, 23];
    
    matrix.forEach(row => {
        if (!keyHorizons.includes(row.horizon)) return;
        
        const tr = document.createElement('tr');
        let tds = `<td class="font-bold">${row.horizon}</td>`;
        firstRowKeys.forEach(k => {
            const val = row.contributions[k];
            tds += `<td class="font-mono">${(val * 100).toFixed(2)}%</td>`;
        });
        tr.innerHTML = tds;
        tableBody.appendChild(tr);
    });
    
    // Update FEVD Chart
    updateFEVDChart(varName);
}


